SELECT DISTINCT
    gv.TENGV AS [HỌ TÊN],
    hv.TENHV AS [HỌC VỊ],
    hh.TENHH AS [HỌC HÀM],
    gv.SODT AS [SỐ ĐIỆN THOẠI],
    gv.DIACHI AS [ĐỊA CHỈ]
FROM
    GIAOVIEN gv,	
	HOCVI hv,
	GV_HV_CN gvhvcn,
	HOCHAM hh
WHERE
	gv.MSHH = hh.MSHH 
	AND 
	gv.MSGV = gvhvcn.MSGV
	AND 
	hv.MSHV = gvhvcn.MSHV

