CREATE PROCEDURE PROC_TENGV_SODTHD_SODTPB
	@TENGV NVARCHAR(30),@SODTHD INT OUTPUT,@SODTPB INT OUTPUT
AS
BEGIN
	IF EXISTS(SELECT * FROM GIAOVIEN WHERE TENGV = @TENGV)
